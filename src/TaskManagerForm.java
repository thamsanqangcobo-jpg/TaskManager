    private void jBtnExportCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExportCSVActionPerformed
        // TODO add your handling code here:
         Path path = Paths.get("C:/Users/Thami/Tasks.csv");

    try {
        // Prepare the header for CSV
        String header = "ID,Task Name,Description,Completion Status,Category\n";
        
        // Prepare the rows
        StringBuilder sb = new StringBuilder();
        sb.append(header);
        
        for (Task<Integer> task : taskList) {
            sb.append(task.getId()).append(',')
              .append(task.getTaskName()).append(',')
              .append(task.getDescription()).append(',')
              .append(task.isCompletionStatus()).append(',')
              .append(task.getCategory()).append('\n');
        }
        
        // Write data to CSV file
        Files.write(path, sb.toString().getBytes(StandardCharsets.UTF_8));
        
        // Notify user
        JOptionPane.showMessageDialog(this, "Tasks exported successfully to CSV.", "Export Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException ex) {
        // Handle the exception
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error exporting tasks to CSV: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBtnExportCSVActionPerformed

    private void jBtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSearchActionPerformed
        // TODO add your handling code here:
        String searchTerm = jSearch.getText().toLowerCase();
    DefaultTableModel tableModel = (DefaultTableModel) jTableTasks.getModel();
    
    // Clear existing rows
    tableModel.setRowCount(0);
    
    for (Task<Integer> task : taskList) {
        if (task.getTaskName().toLowerCase().contains(searchTerm) ||
            task.getDescription().toLowerCase().contains(searchTerm) ||
            task.getCategory().toLowerCase().contains(searchTerm)) {
            
            tableModel.addRow(new Object[]{
                task.getId(), 
                task.getTaskName(), 
                task.getDescription(), 
                task.isCompletionStatus(), 
                task.getCategory()
            });
        }
    }
    }//GEN-LAST:event_jBtnSearchActionPerformed

