    private void jBtnAddTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddTaskActionPerformed
        // TODO add your handling code here:
         // Gather user input from text fields and combo box
        String taskName = jTaskName.getText();
        String description = jTaskDescription.getText();
        String category = jComboCategory.getSelectedItem().toString();

        // Validate user input (Ensure taskName and description are not empty)
        if (taskName.isEmpty() || description.isEmpty()) {
            // Display an error message to the user if either field is empty
            JOptionPane.showMessageDialog(this, "Task Name and Description are required.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method if validation fails
        }
        
        // Generate a unique task ID using the counter
        int taskId = taskIdCounter++;

        // Create a Task object and add it to the ArrayList
        Task<Integer> newTask = new Task<>(taskId, taskName, description, false, category);
        taskList.add(newTask);

        // Database connection parameters
        String url = "jdbc:sqlite:C:/Users/Thami/TaskManagerDB.db/"; // Connection string
        String sql = "INSERT INTO TaskManager (task_name, Description, Completion_status, Category) VALUES (?, ?, ?, ?)"; // SQLite query

        try (Connection connection = DriverManager.getConnection(url);
             PreparedStatement preparedStatement = connection.prepareStatement(sql)) {

            // Set parameters for the SQL statement/query
            preparedStatement.setString(1, taskName);
            preparedStatement.setString(2, description);
            preparedStatement.setBoolean(3, false); // Initial completion status is set to false
            preparedStatement.setString(4, category);

            // Execute the SQL statement/query
            preparedStatement.executeUpdate();

            // Clear input fields
            jTaskName.setText("");
            jTaskDescription.setText("");
            jComboCategory.setSelectedIndex(0);

            // Display a success message to the user
            JOptionPane.showMessageDialog(this, "Task added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            // Handle/Validate database-related exceptions (e.g., connection failure, SQL errors)
            ex.printStackTrace();
            // Display an error message to the user
            JOptionPane.showMessageDialog(this, "Error adding task: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }                                       
    }//GEN-LAST:event_jBtnAddTaskActionPerformed

    private void jBtnViewTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnViewTaskActionPerformed
        // TODO add your handling code here:
         String url = "jdbc:sqlite:C:/Users/Thami/TaskManagerDB.db/"; // Connection string
        String sql = "SELECT * FROM TaskManager"; // SQLite query

        try (Connection connection = DriverManager.getConnection(url);
             Statement statement = connection.createStatement();
             ResultSet resultSet = statement.executeQuery(sql)) {

            // Create a DefaultTableModel to hold the task data
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.addColumn("ID");
            tableModel.addColumn("Task Name");
            tableModel.addColumn("Description");
            tableModel.addColumn("Completion Status");
            tableModel.addColumn("Category");

            // Populate the table model with task data from the database
            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String taskName = resultSet.getString("task_name");
                String description = resultSet.getString("description");
                boolean completionStatus = resultSet.getBoolean("completion_status");
                String category = resultSet.getString("category");

                // Add a row to the table model for each task
                tableModel.addRow(new Object[]{id, taskName, description, completionStatus, category});
            }

            // Set the table model for the JTable
            jTableTasks.setModel(tableModel);

        } catch (SQLException ex) {
            // Handle database-related exceptions (e.g., connection failure, SQL errors)
            ex.printStackTrace();
            // Display an error message to the user
            JOptionPane.showMessageDialog(this, "Error viewing task: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtnViewTaskActionPerformed

