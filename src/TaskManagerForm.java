    private void jBtnReadTasksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnReadTasksActionPerformed
        // TODO add your handling code here:
        // Define the file path
    String filePath = "tasks.txt";

    // Clear the existing task list
    taskList.clear();

    try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
        String line;

        // Read each line from the file
        while ((line = reader.readLine()) != null) {
            String[] parts = line.split("\\|"); // Adjust the delimiter as necessary

            // Ensure the parts array has the expected length
            if (parts.length == 5) {
                // Create a new Task object and add it to the list
                Task<Integer> task = new Task<>(
                    Integer.parseInt(parts[0]),   // ID
                    parts[1],                     // Task Name
                    parts[2],                     // Description
                    Boolean.parseBoolean(parts[3]),// Completion Status
                    parts[4]                      // Category
                );
                taskList.add(task);
            }
        }

        // Optionally update the JTable to reflect the new tasks
        updateTaskTable();

    } catch (IOException | NumberFormatException e) {
        // Handle exceptions
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error reading tasks: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBtnReadTasksActionPerformed

